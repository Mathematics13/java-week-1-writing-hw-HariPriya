package homework_week_5;

/**
 * Program 10
 * Write a Java Program that takes two numbers as input and display the product of two numbers
 * Test Data:
 * Input first number : 25
 * Input second number : 5
 * Expected Output : 25 * 5 = 125
 */
public class Program_10_ProductOfTwoNumbers
{
    public static void main(String[] args)
    {
        int firstNumber = 25;         //First Number declaration
        int secondNumber = 5;         //Second Number declaration
        int product = firstNumber * secondNumber;
        System.out.println(firstNumber + " * " + secondNumber + "  =  " + product); //Expected Output
    }
}
